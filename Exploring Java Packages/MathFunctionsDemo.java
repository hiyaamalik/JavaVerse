import java.lang.*;

public class MathFunctionsDemo {
    public static void main(String[] args) {
        double randomNumber = Math.random();
        System.out.println("Random Number: " + randomNumber);
        
        int absNum = Math.abs(-7888);
        System.out.println("Absolute Number: " + absNum);
        
        double baseExpo = Math.pow(2,8);
        System.out.println("2 raise to power 8: " + baseExpo);
    }
}
